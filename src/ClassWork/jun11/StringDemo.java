package ClassWork.jun11;

public class StringDemo {
    public static void main(String[] args) {

        /*
       two ways to create String :
       1. Literal -> String name = "Sonal";
       2. creating String object using new -> String name = new String("sonal");
         */

        // string creation using literal
        String name = "Sonal";
        System.out.println(name);

        // string creation using new keyword
        String companyName = new String("Pragra");
        System.out.println(companyName);
    }
}
